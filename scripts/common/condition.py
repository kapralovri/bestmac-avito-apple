# ─── Пороги (можно переопределить из config/env через scanner) ───────────────
BATTERY_HARD = 80   # < 80% здоровья АКБ → reject
BATTERY_SOFT = 85   # 80..84% → suspect
CYCLES_HARD = 1000  # > 1000 циклов → reject (живёт мало)
CYCLES_SOFT = 500   # 500..1000 → suspect

# Здоровье АКБ: число процентов рядом со словом про батарею (в обе стороны).
_BAT_WORD = r"(?:аккумул\w*|акб|батаре\w*|ёмкост\w*|емкост\w*|battery|health|здоровь\w*)"
_BAT_BEFORE = re.compile(_BAT_WORD + r"[^\d%]{0,25}?(\d{2,3})\s*%", re.I)
_BAT_AFTER = re.compile(r"(\d{2,3})\s*%[^\d%]{0,25}?" + _BAT_WORD, re.I)
_CYCLES_RE = re.compile(r"(\d{1,4})\s*(?:цикл|cycle|ц\.)", re.I)


@dataclass
class ConditionReport:
    verdict: str = "ok"                       # "ok" | "suspect" | "reject"
    hard: List[str] = field(default_factory=list)
    soft: List[str] = field(default_factory=list)
    positives: List[str] = field(default_factory=list)
    battery_health: Optional[int] = None      # % здоровья, если найдено
    cycles: Optional[int] = None              # циклы заряда, если найдено
    score_delta: int = 0                      # вклад в скоринг сделки

    @property
    def is_ok(self) -> bool:
        return self.verdict == "ok"

    @property
    def is_reject(self) -> bool:
        return self.verdict == "reject"

    def summary(self) -> str:
        """Короткая строка для Telegram."""
        if self.is_reject:
            return "⛔ " + ", ".join(self.hard[:3])
        parts = []
        if self.battery_health is not None:
            parts.append(f"АКБ {self.battery_health}%")
        if self.cycles is not None:
            parts.append(f"{self.cycles} циклов")
        if self.soft:
            parts.append("⚠️ " + ", ".join(self.soft[:2]))
        elif self.positives and self.verdict == "ok":
            parts.append("✅ " + self.positives[0])
        elif self.verdict == "ok":
            parts.append("✅ явных проблем нет")
        return " • ".join(parts) if parts else "—"


def _extract_battery_health(text: str) -> Optional[int]:
    """Возвращает минимальный правдоподобный % здоровья АКБ из текста (или None)."""
    found: list[int] = []
    for rx in (_BAT_BEFORE, _BAT_AFTER):
        for m in rx.finditer(text):
            v = int(m.group(1))
            if 50 <= v <= 100:    # 100% и ниже — правдоподобно; >100 и <50 — мусор/иное
                found.append(v)
    return min(found) if found else None


def _extract_cycles(text: str) -> Optional[int]:
    m = _CYCLES_RE.search(text)
    if not m:
        return None
    v = int(m.group(1))
    return v if 0 <= v <= 5000 else None


def analyze_condition(
    text: str,
    *,
    battery_hard: int = BATTERY_HARD,
    battery_soft: int = BATTERY_SOFT,
    cycles_hard: int = CYCLES_HARD,
    cycles_soft: int = CYCLES_SOFT,
) -> ConditionReport:
    """Анализирует состояние лота по полному тексту объявления."""
    t = (text or "").lower().replace("\xa0", " ")
    r = ConditionReport()

    # Семантика: ловим подстроки. Дедуплицируем человекочитаемые причины.
    for needle, label in HARD_DEFECTS:
        if needle in t and label not in r.hard:
            r.hard.append(label)
    for needle, label in SOFT_DEFECTS:
        if needle in t and label not in r.soft:
            r.soft.append(label)
    for needle, label in POSITIVE_SIGNALS:
        if needle in t and label not in r.positives:
            r.positives.append(label)

    r.battery_health = _extract_battery_health(t)
    r.cycles = _extract_cycles(t)

    # ── Вердикт ──────────────────────────────────────────────────────────────
    reject = bool(r.hard)
    if r.battery_health is not None and r.battery_health < battery_hard:
        reject = True
        r.hard.append(f"АКБ {r.battery_health}%")
    if r.cycles is not None and r.cycles > cycles_hard:
        reject = True
        r.hard.append(f"{r.cycles} циклов")

    suspect = False
    if not reject:
        if r.soft:
            suspect = True
        if r.battery_health is not None and r.battery_health < battery_soft:
            suspect = True
        if r.cycles is not None and r.cycles > cycles_soft:
            suspect = True

    if reject:
        r.verdict = "reject"
        r.score_delta = -100   # фактически не дойдёт до скоринга (отсеивается раньше)
    elif suspect:
        r.verdict = "suspect"
        r.score_delta = -25
    else:
        r.verdict = "ok"
        # Чисто + явный позитив (гарантия/чек/комплект) → выше уверенность
        r.score_delta = 18 if r.positives else 12

    return r
